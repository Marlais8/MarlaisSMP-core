package net.marlais.core.model;

/**
 * Перечисление возможных состояний модуля в системе.
 */
public enum ModuleState {
    /** Модуль обнаружен в системе, но еще не инициализирован. */
    DISCOVERED,
    
    /** Модуль успешно загружен и инициализирован. */
    LOADED,
    
    /** Модуль активен и выполняет свои функции. */
    ACTIVE,
    
    /** Произошла критическая ошибка при инициализации или работе модуля. */
    FAILED,
    
    /** Модуль был безопасно отключен. */
    DISABLED
}